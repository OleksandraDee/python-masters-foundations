import re

def normalize_phone(phone_number):
    # Видалення всіх символів, крім цифр і +
    cleaned = re.sub(r'[^\d+]', '', phone_number.strip())

    # Якщо номер вже починається з '+380' — залишаємо як є
    if cleaned.startswith('+380'):
        return cleaned

    # Якщо номер починається з '380' — додаємо '+' на початок
    if cleaned.startswith('380'):
        return '+' + cleaned

    # Якщо номер починається з '0' або не має коду — додаємо '+38' перед номером
    if cleaned.startswith('0'):
        return '+38' + cleaned

    # У всіх інших випадках (напр., без 0 або з інших кодів) — вважаємо, що це український номер
    return '+38' + cleaned

raw_numbers = [
    "067\\t123 4567",
    "(095) 234-5678\\n",
    "+380 44 123 4567",
    "380501234567",
    "    +38(050)123-32-34",
    "     0503451234",
    "(050)8889900",
    "38050-111-22-22",
    "38050 111 22 11   ",
]

sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
print("Нормалізовані номери телефонів для SMS-розсилки:", sanitized_numbers)

